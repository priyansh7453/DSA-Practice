# Method Overloading \
# Method overloading allows multiple methods with the same name but different parameters.
# It is used to perform different operations on different data types.
def add(a,b):
    print(a+b)        
def add(a, b):
    print(a+b) 

add(5, 3);       
add(5.0, 3.0);   

def add(a,b):
    return a+b 